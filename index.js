function add(x,y){
    return (x+y);
}

function subtract(x,y){
    return (x-y);
}

function multiply(x,y){
    return (x*y);
}

function divide(a,b){
    return (a/b);
}

function increment (n){
    return (n+=1);
}

function decrement (n){
    return (n-=1);
}

function makeInt(n){
    return (parseInt(n, 10));
}

function preserveDecimal(n){
    return (parseFloat(n));
}